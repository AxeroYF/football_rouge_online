export function applyTopPlayerProfile(name, overall, attributes) {
  const profile = TOP_PLAYER_PROFILES[name];
  if (!profile) return { attributes, profile: null };
  const deltas = ARCHETYPES[profile.archetype] ?? {};
  const next = { ...attributes };
  for (const [key, delta] of Object.entries({ ...deltas, ...profile.tweaks })) {
    if (!Number.isFinite(delta) || ![
      "passing", "firstTouch", "dribbling", "crossing", "finishing", "longShots", "heading", "setPieces",
      "tackling", "marking", "positioning", "vision", "decisions", "composure", "offBall", "discipline",
      "pace", "acceleration", "strength", "stamina", "agility", "jumping", "workRate", "aggression",
      "goalkeeping", "reflexes",
    ].includes(key)) continue;
    const base = Number(next[key]);
    next[key] = Math.max(1, Math.min(99, Math.round(Number.isFinite(base) ? base + delta : overall + delta)));
  }
  return { attributes: next, profile };
}
